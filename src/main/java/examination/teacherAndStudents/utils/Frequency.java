package examination.teacherAndStudents.utils;

public enum Frequency {
    ONE_TIME,   // Fee is paid once (e.g., admission fee)
    DAILY,      // Fee is paid daily (rare, but possible)
    WEEKLY,     // Fee is paid every week
    MONTHLY,    // Fee is paid every month (common for tuition)
    QUARTERLY,  // Fee is paid every 3 months
    TERMLY,     // Fee is paid per academic term (common in many school systems)
    SEMESTERLY, // Fee is paid twice per academic year
    ANNUALLY    // Fee is paid once per year (e.g., annual sports fee)
}